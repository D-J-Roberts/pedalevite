ProcessingContextNode::ProcessingContextNode ()
:	_pi_id (-1)
,	_side_arr ()
{
	for (auto & side : _side_arr)
	{
		side._nbr_chn     = 0;
		side._nbr_chn_tot = 0;
		for (auto & buf_index : side._buf_arr)
		{
			buf_index = -1;
		}
	}

	for (auto & buf_index : _bypass_buf_arr)
	{
		buf_index = -1;
	}
}



